    <h2>Sobre a Piecing</h2>
    <p>E um framework da Kuzacraft, desenvolvido para facilitar o desenvolvimento de aplicações web modernas e escaláveis.</p>
    <h3>Visão</h3>
    <p>Ser a escolha número um para desenvolvedores que buscam uma solução robusta e fácil de usar para criar aplicações web de alta performance.</p>
    <h3>Missão</h3>
    <p>Fornecer um framework que permita aos desenvolvedores construir aplicações web de forma rápida, eficiente e com uma estrutura clara, promovendo as melhores práticas de desenvolvimento.</p>
    <h3>Valores</h3>
    <ul>
        <li><strong>Inovação:</strong> Estamos sempre buscando novas tecnologias e abordagens para melhorar nosso framework e oferecer as melhores soluções aos nossos usuários.</li>
        <li><strong>Comunidade:</strong> Valorizamos a colaboração e o suporte mútuo entre os membros da nossa comunidade de desenvolvedores.</li>
        <li><strong>Qualidade:</strong> Comprometemo-nos a fornecer um framework de alta qualidade, com código limpo e bem documentado.</li>
        <li><strong>Facilidade de Uso:</strong> Nosso objetivo é tornar o desenvolvimento web acessível a todos, independentemente do nível de experiência.</li>
    </ul>
    <h3>Equipe</h3>
    <p>A equipe da Piecing é composta por desenvolvedores apaixonados por tecnologia e comprometidos em criar um framework que atenda às necessidades dos desenvolvedores modernos. Estamos sempre abertos a feedbacks e sugestões para melhorar nosso produto e garantir que ele continue sendo uma ferramenta valiosa para a comunidade de desenvolvimento web.</p>

